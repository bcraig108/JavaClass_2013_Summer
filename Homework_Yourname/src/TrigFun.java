class TrigFun{
    public static void main( String[] args ){
        double x = 3.14/2.0;
        double y = Math.cos( x );
        double z = Math.sin( x );
        System.out.println( "The cosine of " + x + " is " + y + "." );
        System.out.println( "The sine of " + x + " is " + z + "." );
    }
}
